public class ZmiennaChar
{
    public static void main(String[] args) {
        char letter ='a';
        System.out.println(letter);
        System.out.println(letter++);
        System.out.println(letter++);
        System.out.println(letter--);
        System.out.println(letter);
        System.out.println(letter--);
        System.out.println(letter);
        System.out.println(--letter);
        System.out.println(--letter);
        System.out.println(letter);
        System.out.println(letter++);
        System.out.println(letter);
    }
}
